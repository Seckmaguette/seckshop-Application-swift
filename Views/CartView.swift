
import SwiftUI

struct CartView: View {
    @EnvironmentObject var cartManager: CartManager
    
    var body: some View {
        ScrollView {
            if cartManager.paymentSuccess {
                Text("Merci pour votre achat! Vous vous sentirez bientôt à l'aise dans nos produits confortables ! Vous recevrez également un e-mail de confirmation sous peu.")
                    .padding()
            } else {
                if cartManager.products.count > 0 {
                    ForEach(cartManager.products, id: \.id) { product in
                        ProductRow(product: product)
                    }
                    
                    HStack {
                        Text("Le total de votre panier est de")
                        Spacer()
                        Text("\(cartManager.total)F")
                            .bold()
                    }
                    .padding()
                    
                    PaymentButton(action: cartManager.pay)
                        .padding()
                    
                } else {
                    Text("Votre panier est vide")
                }
            }
        }
        .navigationTitle(Text("Mon Panier"))
        .padding(.top)
        .onDisappear {
            if cartManager.paymentSuccess {
                cartManager.paymentSuccess = false
            }
        }
    }
}

